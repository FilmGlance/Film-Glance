/** Deduplicate a movie list (case-insensitive) */
export function deduplicateMovies(movies: string[]): string[] {
  const seen = new Set<string>();
  return movies.filter((m) => {
    const key = m.toLowerCase().trim();
    if (seen.has(key)) return false;
    seen.add(key);
    return true;
  });
}

